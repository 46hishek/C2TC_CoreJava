package com.string.create;

public class EndWith {

	public static void main(String[] args) {
		String s="First newJob";
		System.out.println(s.endsWith("b"));
		System.out.println(s.endsWith("Job"));
		System.out.println(s.endsWith("st"));
		System.out.println(s.endsWith("JOB"));
	}

}
